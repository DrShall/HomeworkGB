//Каждый 4-й год является високосным, кроме каждого 100-го, при этом каждый
//400-й – високосный. Вывести результаты работы программы в консоль.
//==============================================================================
void Task5()
{
	int year;
	bool isLeap = false;
	cout << "Введите год: ";
	cin >> year;
	if(year%4 == 0)
	{
		if(year % 400 == 0)
			isLeap = true;
		else if(year % 100 == 0)
			isLeap = false;
	}
	if(isLeap)
		cout << year << " год является високосным" << endl;
	else
		cout << year << " год не является високосным" << endl;
}

