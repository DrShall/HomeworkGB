//==============================================================================
//    Задание 1
// Написать программу, проверяющую что сумма двух (введенных с клавиатуры)
//чисел лежит в пределах от 10 до 20 (включительно), если да – вывести
//строку "true", в противном случае – "false";
//==============================================================================

void Task1()
{
	int a, b;
	cout << "Введите два числа: ";
	cin >> a >> b;
	if(a+b >= 10 && a+b <= 20)
		cout << "true" << endl;
	else
		cout << "false" << endl;
}

