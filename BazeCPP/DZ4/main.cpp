//==============================================================================
//    Задание 4
// Со звёздочкой. Написать программу, проверяющую, является ли некоторое
//число - простым. Простое число — это целое положительное число, которое делится
//без остатка только на единицу и себя само.
//==============================================================================
void Task4()
{
	int var;
	cout << "Введите любое положительное число: ";
	cin >> var;
	bool isSimple = true;
	if(var < 4)
		isSimple = true;
	else if(var % 2 == 0)
		isSimple = false;
	else
		for(int i = 3; i < var; ++i)
		{
			if(var % i == 0)
			{
				isSimple = false;
				break;
			}
		}
	if(isSimple)
		cout << "Число " << var << " простое." << endl;
	else
		cout << "Число " << var << " не является простым" << endl;
}

